void main() {
  int num = 10;

  num++;
  print(num); // Output: 11
  num--;
  print(num); // Output: 10

  num = 10;
  num += 5;
  print(num); // Output: 15

  num = 10;
  num -= 3;
  print(num); // Output: 7

  num = 10;
  num *= 2;
  print(num); // Output: 20

  num = 10;
  num ~/= 4;
  print(num); // Output: 2

  num = 10;
  num %= 3;
  print(num); // Output: 1
}
